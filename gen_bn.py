def generate_bn_word():
    """Generate a pronounceable Bengali word using syllable patterns."""
    num_syllables = random.choice([2, 2, 2, 3, 3, 3, 4])
    word = ""

    # Optionally start with an independent vowel
    if random.random() < 0.15:
        word += random.choice(bn_vowels_independent)
        num_syllables -= 1
        if num_syllables <= 0:
            num_syllables = 1

    for i in range(num_syllables):
        # Occasionally use conjunct syllable
        if random.random() < 0.15:
            word += random.choice(bn_conjunct_syllables)
        else:
            word += random.choice(bn_syllables)

    # Optionally add an ending
    if random.random() < 0.35:
        word += random.choice(bn_endings)

    return word


def derive_bn_words(base, count=5):
    """Create derived forms from a Bengali base word."""
    derived = set()

    # Add prefixes
    for p in random.sample(bn_prefixes, min(4, len(bn_prefixes))):
        w = p + base
        if 3 <= len(w) <= 10:
            derived.add(w)

    # Add noun suffixes
    for s in random.sample(bn_noun_suffixes, min(3, len(bn_noun_suffixes))):
        w = base + s
        if 3 <= len(w) <= 10:
            derived.add(w)

    # Add adjective suffixes
    for s in random.sample(bn_adj_suffixes, min(2, len(bn_adj_suffixes))):
        w = base + s
        if 3 <= len(w) <= 10:
            derived.add(w)

    return list(derived)[:count]


