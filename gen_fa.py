def generate_fa_word():
    """Generate a pronounceable Persian word using syllable patterns."""
    method = random.choice([1, 2, 3, 4, 5])

    if method == 1:
        # start + CV syllables + ending
        word = random.choice(fa_word_starts)
        num_mid = random.choice([0, 1, 1, 2])
        for _ in range(num_mid):
            word += random.choice(fa_syllables)
        if random.random() < 0.6:
            word += random.choice(fa_word_ends)
        return word

    elif method == 2:
        # CV + CVC pattern
        word = random.choice(fa_syllables)
        if random.random() < 0.7:
            word += random.choice(fa_cvc_syllables)
        else:
            word += random.choice(fa_syllables)
        return word

    elif method == 3:
        # bigram + syllable + optional ending
        word = random.choice(fa_bigrams)
        word += random.choice(fa_syllables)
        if random.random() < 0.4:
            word += random.choice(fa_word_ends)
        return word

    elif method == 4:
        # Multiple CV syllables
        num_syl = random.choice([2, 2, 3, 3])
        word = ""
        for _ in range(num_syl):
            word += random.choice(fa_syllables)
        if random.random() < 0.3:
            word += random.choice(fa_common_endings)
        return word

    else:
        # CVC + CV pattern
        word = random.choice(fa_cvc_syllables)
        word += random.choice(fa_syllables)
        if random.random() < 0.3:
            word += random.choice(fa_common_endings)
        return word


def is_valid_persian(word):
    """Check if a word contains only valid Persian characters."""
    valid_chars = set('آابپتثجچحخدذرزژسشصضطظعغفقکگلمنوهی')
    return all(c in valid_chars for c in word)


def derive_fa_words(base, count=6):
    """Create derived forms from a Persian base word."""
    derived = set()

    # Add prefixes
    for p in random.sample(fa_prefixes, min(5, len(fa_prefixes))):
        w = p + base
        if 3 <= len(w) <= 10 and is_valid_persian(w):
            derived.add(w)

    # Add noun suffixes
    for s in random.sample(fa_noun_suffixes, min(4, len(fa_noun_suffixes))):
        w = base + s
        if 3 <= len(w) <= 10 and is_valid_persian(w):
            derived.add(w)

    # Add adjective suffixes
    for s in random.sample(fa_adj_suffixes, min(3, len(fa_adj_suffixes))):
        w = base + s
        if 3 <= len(w) <= 10 and is_valid_persian(w):
            derived.add(w)

    # Add abstract suffixes
    for s in fa_abstract_suffixes:
        w = base + s
        if 3 <= len(w) <= 10 and is_valid_persian(w):
            derived.add(w)

    return list(derived)[:count]


