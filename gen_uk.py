def generate_uk_word():
    """Generate a pronounceable Ukrainian word."""
    num_syllables = random.choice([2, 2, 2, 3, 3, 3, 4])
    word = ""
    for i in range(num_syllables):
        word += random.choice(uk_syllables)
    # Optionally add ending
    if random.random() < 0.4:
        word += random.choice(uk_endings)
    return word


def derive_uk_words(base, count=5):
    """Create derived forms from a Ukrainian base word."""
    derived = set()

    # Add prefixes
    for p in random.sample(uk_prefixes, min(3, len(uk_prefixes))):
        w = p + base
        if 3 <= len(w) <= 10:
            derived.add(w)

    # Get root by stripping common endings
    root = base
    # Strip common Ukrainian endings
    for ending in ['ати', 'яти', 'ити', 'іти', 'ути', 'ість', 'ство',
                   'ння', 'ний', 'ській', 'ський', 'овий', 'ий', 'ій',
                   'ка', 'ок', 'ець', 'ик', 'ня', 'тя', 'ця',
                   'а', 'о', 'е', 'и', 'і', 'у', 'я', 'ю']:
        if base.endswith(ending) and len(base) - len(ending) >= 2:
            root = base[:-len(ending)]
            break

    if len(root) >= 2:
        all_suffixes = uk_noun_suffixes + uk_adj_suffixes
        for s in random.sample(all_suffixes, min(4, len(all_suffixes))):
            w = root + s
            if 3 <= len(w) <= 10:
                derived.add(w)

    return list(derived)[:count]


